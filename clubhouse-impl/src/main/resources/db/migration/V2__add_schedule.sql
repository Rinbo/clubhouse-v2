CREATE TABLE schedule (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  team_id BIGINT,
  period_start date NOT NULL,
  period_end date NOT NULL,
  CONSTRAINT pk_schedule PRIMARY KEY (id)
);

CREATE TABLE training_time (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
  duration BIGINT NOT NULL,
  day_of_week VARCHAR(255) NOT NULL,
  schedule_id BIGINT,
  location VARCHAR(255),
  CONSTRAINT pk_training_time PRIMARY KEY (id)
);

ALTER TABLE schedule ADD CONSTRAINT FK_SCHEDULE_ON_TEAM FOREIGN KEY (team_id) REFERENCES team (id);

ALTER TABLE training_time ADD CONSTRAINT FK_TRAINING_TIME_ON_SCHEDULE FOREIGN KEY (schedule_id) REFERENCES schedule (id);